public class Vehicles{

    private String Type;
    private String VehicleNo;

    public Vehicles(String Type, String VehicleNo){
        this.Type = Type;
        this.VehicleNo = VehicleNo;
    }

    public String getType(){
        return Type;
    }

    public String getVehicleNo(){
        return VehicleNo;
    }



    
}
